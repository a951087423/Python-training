#資料: 程式的基本單位

#字串
"測試中文"
"Hello World"

#布林值
True    #正確
False   #錯誤

#有順序、可變動的列表 List
[3,4,5]
["Hello" , "World"]

#有順序、不可變動的列表 Tuple
(3,4,5)
("Hello" , "World")

#集合 Set
{3,4,5}
{"Hello" , "World"}

#字典 Dictionary
{"apple":"蘋果" , "data":"資料"}

